const pages = Object.keys(LINKS_MAP)
let updated = 0

for (const slug of pages) {
  const pagePath = path.join(ROOT, 'app', slug, 'page.tsx')
  if (!fs.existsSync(pagePath)) {
    console.warn(`  ⚠ Not found: app/${slug}/page.tsx`)
    continue
  }

  let content = fs.readFileSync(pagePath, 'utf8')
  if (!content.includes('links={[]}')) {
    console.log(`  — app/${slug}/page.tsx already has links, skipping`)
    continue
  }

  const linksJson = JSON.stringify(LINKS_MAP[slug])
  content = content.replace('links={[]}', `links={${linksJson}}`)
  fs.writeFileSync(pagePath, content)
  console.log(`  ✓ app/${slug}/page.tsx updated (${LINKS_MAP[slug].length} links)`)
  updated++
}

console.log(`\nDone! Updated ${updated} pages.`)
